    private void BtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistrarActionPerformed
        String id = TFIdProducto.getText();
        String nom = TFNombrep.getText();
        String des = TFDescripcion.getText();
        String preciob = TFPrecioBa.getText();
        String precios = TFPrecioSu.getText();
        String cant = TFCantidad.getText();

        Productos patient = new Productos(id, nom, des, preciob, precios, cant, foto);

        if (patient.insertar(cnx) == 1) {
            JOptionPane.showMessageDialog(this, "Producto agregado");
            cnx.entablar("SELECT * FROM productos", TProductos);
        } else {
            JOptionPane.showMessageDialog(this, "Hubo un error, intente de nuevo");
        }
    }//GEN-LAST:event_BtnRegistrarActionPerformed

    private void LSubirMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LSubirMousePressed
        JFileChooser fc = new JFileChooser(System.getProperty("user.dir"));
        if (fc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File archivo = fc.getSelectedFile();
            foto = new ImageIcon(archivo.getPath());
            LFoto.setIcon(new ImageIcon(cnx.escalarImagen(foto.getImage(), 200, 200)));
        }
    }//GEN-LAST:event_LSubirMousePressed

    private void TProductosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TProductosMousePressed
        int ren = TProductos.getSelectedRow();
        DefaultTableModel modelo = (DefaultTableModel) TProductos.getModel();

        if (ren >= 0) {
            TFIdProducto.setText(modelo.getValueAt(ren, 0).toString());
            TFNombrep.setText(modelo.getValueAt(ren, 1).toString());
            TFDescripcion.setText(modelo.getValueAt(ren, 2).toString());
            TFPrecioBa.setText(modelo.getValueAt(ren, 3).toString());
            TFPrecioSu.setText(modelo.getValueAt(ren, 4).toString());
            TFCantidad.setText(modelo.getValueAt(ren, 5).toString());

            foto = new ImageIcon(cnx.escalarImagen(((ImageIcon) modelo.getValueAt(ren, 6)).getImage(), 197, 188));
            LFoto.setIcon(foto);
        }
    }//GEN-LAST:event_TProductosMousePressed

